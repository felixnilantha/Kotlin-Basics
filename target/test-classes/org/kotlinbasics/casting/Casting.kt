package org.kotlinbasics.casting

fun main(args: Array<String>) {
	// Casting a Double to int
	// print value 6
	println("6.25 to Int : " + (6.25.toInt()))
	
	// Casting Charactor to Int
	// value will be 65
	println(" A to Int : " + ('A'.toInt()))
	
	// Casting Int to Charactor
	// Value will be 'A'
	print (" 65 in Char : " + (65.toChar()))
}
